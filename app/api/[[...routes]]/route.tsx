import { Button, Frog, TextInput } from 'frog'
import { devtools } from 'frog/dev'
// import { neynar } from 'frog/hubs'
import { handle } from 'frog/next'
import { serveStatic } from 'frog/serve-static'

const app = new Frog({
  assetsPath: '/',
  basePath: '/api',
  // Supply a Hub to enable frame verification.
  // hub: neynar({ apiKey: 'NEYNAR_FROG_FM' })
})

// Uncomment to use Edge Runtime
// export const runtime = 'edge'

app.frame('/', (c) => {
  const { buttonValue, inputText, status } = c
  const fruit = inputText || buttonValue
  return c.res({
    image: (
      <div
        style={{
          alignItems: 'center',
          background:
            status === 'response'
              ? 'linear-gradient(to right, #432889, #17101F)'
              : 'black',
          backgroundSize: '100% 100%',
          display: 'flex',
          flexDirection: 'column',
          flexWrap: 'nowrap',
          height: '100%',
          justifyContent: 'center',
          textAlign: 'center',
          width: '100%',
        }}
      >
        <div
          style={{
            color: 'white',
            fontSize: 60,
            fontStyle: 'normal',
            letterSpacing: '-0.025em',
            lineHeight: 1.4,
            marginTop: 30,
            padding: '0 120px',
            whiteSpace: 'pre-wrap',
          }}
        >
          {status === 'response'
            ? `Nice choice.${fruit ? ` ${fruit.toUpperCase()}!!` : ''}`
            : 'Welcome!'}
        </div>
      </div>
    ),
    intents: [
      <TextInput placeholder="Enter custom fruit..." />,
      <Button value="apples">Apples</Button>,
      <Button value="oranges">Oranges</Button>,
      <Button value="bananas">Bananas</Button>,
      status === 'response' && <Button.Reset>Reset</Button.Reset>,
    ],
  })
})
app.frame('/trx/:hash', async (c) => {
  const { hash } = c.req.param();

  // Fetch transaction details using the provided hash
  const response = await fetch(`https://sepolia.explorer.mode.network/api/v2/transactions/${hash}`);
  if (!response.ok) {
   
    return c.res({
      image: (
        <div style={{ color: 'red', fontSize: '20px' }}>
          Error fetching transaction details.
        </div>
      ),
    });
  }
  const transaction = await response.json();


  return c.res({
    image: (
      <div style={{ color: 'white', display: 'flex', fontSize: '20px', flexDirection: 'column', padding: '20px' }}>
        
        <div>Transaction Hash: {transaction.hash}</div>
        <div>Status: {transaction.status}</div>
        <div>Gas Used: {transaction.gas_used}</div>
      
      </div>
    ),
  });
});

devtools(app, { serveStatic })

export const GET = handle(app)
export const POST = handle(app)

